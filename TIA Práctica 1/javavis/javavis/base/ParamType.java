package javavis.base;

/**
 * Enum to manage parameter types
 */
public enum ParamType {
	/** boolean param. */
	BOOL,

	/** integer param. */
	INT,

	/** float  param. */
	FLOAT,

	/** string param. */
	STRING,

	/** image param. */
	IMAGE,

	/** list param. */
	LIST,
	
	/** Enum param. */
	ENUM,

	/** object param. */
	OBJECT,

	/** file param. */
	FILE,

	/** dir param. */
	DIR, 
	
	/** screen data param (3D). */
	SCRDATA
}
